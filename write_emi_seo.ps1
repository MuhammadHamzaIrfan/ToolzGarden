## 3.4 H2: What Is EMI?

An Equated Monthly Installment (EMI) is a fixed payment made by a borrower to a lender on a
specific date every calendar month. EMIs are used to repay both the principal (the amount originally
borrowed) and the interest (the lender's charge for providing credit) over the agreed loan tenure.

EMIs are structured so that the total repayment is spread evenly across months, making financial
planning straightforward. In the early months of a loan, a larger portion of your EMI goes toward
paying interest. As the loan matures, progressively more of your payment reduces the principal —
this is the foundation of the reducing balance method, the standard used by banks globally.

For example, if you borrow Rs. 5,00,000 at 10% per annum for 5 years (60 months), your EMI would
be approximately Rs. 10,624 per month. Over the entire tenure, you would pay Rs. 6,37,480 in total
— meaning Rs. 1,37,480 is the total interest cost.

Key takeaway: A lower interest rate and shorter tenure means a lower total interest outflow, even
if the monthly EMI is slightly higher.

