## 3.10 H2: Tips to Reduce Your EMI

1. Make a Larger Down Payment: The less you borrow, the lower your EMI.
2. Choose a Longer Tenure: Lowers EMI but increases total interest paid — balance carefully.
3. Negotiate a Lower Interest Rate: Even 0.5% difference saves significantly over long tenure.
4. Make Prepayments: Extra payments reduce outstanding principal, lowering future interest.
5. Transfer to a Lower-Rate Lender: Consider refinancing via balance transfer when rates drop.
6. Improve Your Credit Score: Scores above 750 often qualify for lower interest rates.
7. Avoid Rolled-In Processing Fees: Some lenders add fees to loan amount, inflating EMI base.

