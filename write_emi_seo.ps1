## 3.9 H2: Reducing Balance vs Flat Rate EMI

### Reducing Balance Method (Standard)
Used by all RBI-regulated banks. Interest is calculated on the outstanding principal each month.
As you repay the principal, the interest component shrinks.

Example: Rs.1,00,000 loan at 12% for 1 year
  EMI = Rs.8,885/month | Total Interest = Rs.6,620

### Flat Rate Method (Common in Dealer Financing)
Interest is calculated on the original principal for the entire tenure, regardless of repayments.
This results in a significantly higher effective interest cost.

Example: Rs.1,00,000 loan at 12% flat for 1 year
  EMI = Rs.9,000/month | Total Interest = Rs.8,000

| Comparison | Reducing Balance | Flat Rate |
|-----------|-----------------|-----------|
| Interest basis | Outstanding principal | Original principal |
| Effective rate | ~21-22% effective (for 12% flat) | 12% nominal |
| Total interest | Rs.6,620 | Rs.8,000 |
| Used by | All RBI-regulated banks | Some dealers, NBFCs |

WARNING: Always ask your lender whether they quote flat rate or reducing balance.
A 12% flat rate is equivalent to approximately 21% reducing balance — almost double!

