## 3.8 H2: EMI Formula Explained

The standard EMI formula used by all banks and financial institutions is:

  EMI = [P x r x (1 + r)^n] / [(1 + r)^n - 1]

Where:
  P = Principal loan amount
  r = Monthly interest rate = Annual rate divided by 12 divided by 100
  n = Loan tenure in months

### Worked Example

Loan: Rs.5,00,000 | Rate: 10% per annum | Tenure: 5 years (60 months)

  Step 1: r = 10 / 12 / 100 = 0.008333
  Step 2: (1 + r)^n = (1.008333)^60 = 1.6453
  Step 3: Numerator = 5,00,000 x 0.008333 x 1.6453 = 6,855.48
  Step 4: Denominator = 1.6453 - 1 = 0.6453
  Step 5: EMI = 6,855.48 / 0.6453 = Rs.10,624 per month

  Total Payment = Rs.10,624 x 60 = Rs.6,37,440
  Total Interest = Rs.6,37,440 - Rs.5,00,000 = Rs.1,37,440

Special case: If the interest rate is 0%, EMI = P / n (simple division). Our calculator handles this automatically.

