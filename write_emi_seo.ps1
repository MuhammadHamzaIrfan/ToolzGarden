--- EMI CALCULATION LOGIC ---

  function calculateEMI(principal, annualRate, tenureMonths) {
    if (annualRate === 0) return principal / tenureMonths;
    const r = annualRate / 12 / 100;
    const n = tenureMonths;
    return (principal * r * Math.pow(1 + r, n)) / (Math.pow(1 + r, n) - 1);
  }

  After calculating EMI:
    totalPayment = emi * tenureMonths
    totalInterest = totalPayment - principal
    principalPercent = (principal / totalPayment) * 100
    interestPercent = (totalInterest / totalPayment) * 100

  Update DOM: emi-display, total-payment, total-interest
  Animate breakdown bar: set principal-bar width to principalPercent%, interest-bar to interestPercent%
  Update percentage labels next to bar
  Regenerate amortization table if visible

